var searchInsert = function(nums, target) {
    let start = 0, end = nums.length - 1, mid;

    while (end - start > 3) {
        mid = start + Math.floor((end - start) >> 1);
        if (nums[mid] < target) {
            // å³åŠåŒº
            start = mid + 1;
        } else {
            // å·¦åŠåŒº
            end = mid;
        }
    }

    for (let i = 0; i <= end; i++) {
        if (nums[i] >= target) {
            return i;
        }
    }

    return nums.length;
};
